package com.kh.operator;

public class Arithmetic {

	public static void main(String[] args) {
		// 1. 산술연산자 : 더하기(+), 뺴기(-), 곱하기(*), 나머지 몫(/), 나누기 나머지(%)
		int num1 = 5;
		int num2 = 2;
		System.out.println(num1 +num2);
		System.out.println(num1 - num2);
		System.out.println(num1 * num2);
		System.out.println(num1 / num2);
		System.out.println(num1 % num2);

	}

}
