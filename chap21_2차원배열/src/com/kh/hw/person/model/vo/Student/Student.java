package com.kh.hw.person.model.vo.Student;

public class Student {

	private String name;
	private int age;
	private String major;
	private int grade;
	private double height;
	private double weight;
	
	
	public Student(String name,int age,String major,int grade,double height,double weight) {
		this.name = name;
		this.age = age;
		this.height = height;
		this.major = major;
		this.weight = weight;
		this.grade = grade;
		
		
	}
		
}



